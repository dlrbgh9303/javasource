
public class PrintMain {
// 모든 코드는 이 영역에 작성해야함
	public static void main(String[] args) {
//  여기다 쓴것만 실행
//	콘솔창에 출력
		System.out.print("에어콘 리모콘이 어디 있을텐데");
//
// 콘솔창에 출력하고 나서 줄 바꿈
		System.out.println("좀 찾아보시죠 종용씨");
//	코드 외우지 마라 하도 작업해서 외움을 당하게됨
//  형식을 지정하면서 콘솔창에 출력   
		System.out.printf("%.1f", 45.123124);
		System.out.println("지하철 1호선");
		
//		
//	특수문자 쓸때에 역슬래시 사용
//		\n -new line 줄만바
//		\r -carriage return 커서를 맨앞으
//		\r\n - 완성된 줄바꿈
//		\n만 있어도알아서 줄바꿈이 완성되는 프로그램도 존재		
//		\t  탭
//		\\ = \ 로 쳐준다
		System.out.println("1850원");
		System.out.println("ㅋㅋㅋ\nㅎㅎㅎ");
		System.out.println("집:\t분당");
		System.out.println("직업:\t강사");
		System.out.println("ㅋㅋㅋ:\b강사");
		System.out.println(" \" ");

	}

}
